const sanitize = require('sanitize-html')
const htmlTags = require('html-tags')

const allowedTags = htmlTags.filter(
  (t) => !['title', 'textarea', 'style', 'xmp', 'iframe', 'noembed', 'noframes', 'script', 'plaintext'].includes(t),
)

const attributes = [
  'class',
  'id',
  'abbr',
  'accept',
  'accept-charset',
  'accesskey',
  'action',
  'align',
  'alt',
  'aria-describedby',
  'aria-hidden',
  'aria-label',
  'aria-labelledby',
  'axis',
  'border',
  'cellpadding',
  'cellspacing',
  'char',
  'charoff',
  'charset',
  'checked',
  'clear',
  'cols',
  'colspan',
  'color',
  'compact',
  'coords',
  'datetime',
  'dir',
  'disabled',
  'enctype',
  'for',
  'frame',
  'headers',
  'height',
  'hreflang',
  'hspace',
  'ismap',
  'label',
  'lang',
  'maxlength',
  'media',
  'method',
  'multiple',
  'name',
  'nohref',
  'noshade',
  'nowrap',
  'open',
  'progress',
  'prompt',
  'readonly',
  'rel',
  'rev',
  'role',
  'rows',
  'rowspan',
  'rules',
  'scope',
  'selected',
  'shape',
  'size',
  'span',
  'start',
  'summary',
  'tabindex',
  'target',
  'title',
  'type',
  'usemap',
  'valign',
  'value',
  'vspace',
  'width',
  'itemprop',
]

module.exports.sanitizer = (html) =>
  sanitize(html, {
    disallowedTagsMode: 'escape',
    allowedSchemes: sanitize.defaults.allowedSchemes.concat(['data']),
    allowedTags,
    allowedAttributes: {
      a: ['href'],
      img: ['src', 'longdesc'],
      div: ['itemscope', 'itemtype'],
      '*': attributes,
    },
  })
